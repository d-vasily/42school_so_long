#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int		i;
	char	tmp;

	if (!s)
		return ;
	i = 0;
	while (s[i])
	{
		tmp = s[i];
		write(fd, &tmp, 1);
		i++;
	}
}
